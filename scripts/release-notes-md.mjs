// Regenerate RELEASE_NOTES.md from src/lib/releaseNotes.ts (the source of
// truth that also feeds /admin/release-notes). Run after adding a release:
//   node scripts/release-notes-md.mjs
import { readFileSync, writeFileSync } from "node:fs";
import { createRequire } from "node:module";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const require = createRequire(import.meta.url);
const ts = require("typescript");

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const src = readFileSync(join(root, "src/lib/releaseNotes.ts"), "utf8");

// Transpile the TS module in-memory and evaluate it to get RELEASES.
const js = ts.transpileModule(src, {
  compilerOptions: { module: ts.ModuleKind.CommonJS, target: ts.ScriptTarget.ES2020 },
}).outputText;
const mod = { exports: {} };
new Function("module", "exports", js)(mod, mod.exports);
const RELEASES = mod.exports.RELEASES;

const TYPE_LABEL = { new: "New", improved: "Improved", fixed: "Fixed" };

let md = `# Release Notes

User-facing changelog for the Project Hub — the same content the team sees in-app at
**Admin → Release Notes** (\`/admin/release-notes\`).

> **Do not edit this file by hand.** The source of truth is
> [\`src/lib/releaseNotes.ts\`](src/lib/releaseNotes.ts); regenerate this file with
> \`node scripts/release-notes-md.mjs\`.

`;

for (const r of RELEASES) {
  md += `## v${r.version} — ${r.date} · ${r.title}\n\n`;
  for (const item of r.items) {
    md += `- **[${TYPE_LABEL[item.type] || item.type}] ${item.title}**`;
    if (item.requestedBy) md += ` _(requested by ${item.requestedBy})_`;
    md += `\n  ${item.detail}\n`;
  }
  md += "\n";
}

writeFileSync(join(root, "RELEASE_NOTES.md"), md);
console.log(`RELEASE_NOTES.md regenerated: ${RELEASES.length} releases (latest v${RELEASES[0].version}).`);
